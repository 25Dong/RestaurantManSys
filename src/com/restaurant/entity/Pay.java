package com.restaurant.entity;

public class Pay {
	private int payNo;
	private int sumMenoy;
	private int sumNumber;
	private String time;
	private String meunNO;
	public String getMeunNO() {
		return meunNO;
	}
	public void setMeunNO(String meunNO) {
		this.meunNO = meunNO;
	}
	public int getPayNo() {
		return payNo;
	}
	public void setPayNo(int payNo) {
		this.payNo = payNo;
	}
	public int getSumMenoy() {
		return sumMenoy;
	}
	public void setSumMenoy(int sumMenoy) {
		this.sumMenoy = sumMenoy;
	}
	public int getSumNumber() {
		return sumNumber;
	}
	public void setSumNumber(int sumNumber) {
		this.sumNumber = sumNumber;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
	

}
